// omit paletteIndex parameter to get from current palette
msa.ColorManager.prototype.getRandomPalette = function() {
	this.currentPalette = THREE.Math.randInt(0, this.palettes.length-1);
	console.log("msa.ColorManager.getRandomPalette", this.palettes[this.currentPalette].name);
	return this.currentPalette;
}

msa.ColorManager.prototype.getCurrentPalette = function() {
	return this.currentPalette;
}

msa.ColorManager.prototype.getRandomHex = function(paletteIndex) {
	if(paletteIndex == undefined) paletteIndex = this.currentPalette;
	return this.palettes[paletteIndex].colors[THREE.Math.randInt(0, this.palettes[paletteIndex].colors.length-1)]
}

msa.ColorManager.prototype.getRandomColor = function(paletteIndex) {
	if(paletteIndex == undefined) paletteIndex = this.currentPalette;
	return new THREE.Color(this.getRandomHex(paletteIndex));
}

msa.ColorManager.prototype.getRandomBGHex = function(paletteIndex) {
	if(paletteIndex == undefined) paletteIndex = this.currentPalette;
	return this.palettes[paletteIndex].bg[THREE.Math.randInt(0, this.palettes[paletteIndex].bg.length-1)]
}

msa.ColorManager.prototype.getRandomBGColor = function(paletteIndex) {
	if(paletteIndex == undefined) paletteIndex = this.currentPalette;
	return new THREE.Color(this.getRandomBGHex(paletteIndex));
}

